// Helper to read nested keys safely
function getTranslation(lang, key) {
  const root = TRANSLATIONS[lang] || TRANSLATIONS['tr'];
  return key.split('.').reduce((o, k) => (o && o[k] !== undefined) ? o[k] : undefined, root);
}

